{
		jQuery("#paper_code-bw").focus();
});

jQuery(".gsmPremium-bw").on('click', function()
{
		jQuery("#paper_code-bw").focus();
});


jQuery(".price-bw").on('click', function()
{
	jQuery(".price-bw.Vcard_active-bw").removeClass('Vcard_active-bw');
	
	setCardPriceBw(jQuery(this).attr('data-price'));
	jQuery("#original_v_card_price-bw").val(jQuery(this).attr('data-price'));
	
	
	setCardTypeBw(jQuery(this).attr('data-card')+"_"+jQuery(this).attr('data-type'));
	
	jQuery("#original_title-bw").val(jQuery(this).attr('data-card')+"_"+jQuery(this).attr('data-type'));
	
	jQuery("#paper_side-bw").val(jQuery(this).attr('data-type'));
	jQuery(this).addClass("Vcard_active-bw");
	
	resetInputBoxBw();
});


function resetInputBoxBw()
{
	jQuery("#v_card_names-bw").val("1");
	jQuery("#paper_code-bw").val("");
}

function setCardPriceBw(value)
{
	jQuery("#v_card_price-bw").val(value);
	jQuery("#show_v_card_price-bw").html("<h2>"+value+"</h2>");
}

function setCardTypeBw(value)
{
	jQuery("#v_card_type-bw").val("V_Card_"+value);
	jQuery("#show_v_card_type-bw").html("<h2>"+value+"</h2>");
	
}

function setQtyBw(value)
{
	jQuery(".rowQty-bw").css('opacity', "1");
	
	
	jQuery("#qty_"+value+" .price-bw").css('opacity', "1");
	jQuery("#qty_"+value+" .price-bw").css('opacity', "1");
	
	//console.log("Element", jQuery("#qty_"+value+" .price"));
	jQuery("#qty_"+value).css('opacity', "1");
	jQuery("#v_card_qty-bw").val(value);
	jQuery("#original_v_card_qty-bw").val(value);
	
	jQuery("#show_v_card_qty-bw").html("<h2>"+value+"</h2>");
}

function getPaperPrintSideBw()
{
	return jQuery("#paper_side-bw").val();
}
function setFinalQtyBw(value)
{
	jQuery("#v_card_qty-bw").val(value);
	jQuery("#show_v_card_qty-bw").html("<h2>"+value+"</h2>");
}

function getCardQtyBw()
{
	return jQuery("#v_card_qty-bw").val();
}

function getCardTypeBw()
{
	var cardType 	= jQuery("#v_card_type-bw").val();
	var paperCode 	= getPaperCodeBw();
	
	
	if(cardType == "V_Card_Code" )
	{
		return "V_Card_Code_"+paperCode;
	}
	
	if(cardType == "V_Card_Code" )
	{
		return "V_Card_Code_"+paperCode;
	}
	
	if(cardType == "V_Card_Code" )
	{
		return "V_Card_Code_"+paperCode;
	}
	
	return jQuery("#v_card_type-bw").val();
}

function getCardPriceBw()
{
	return jQuery("#v_card_price-bw").val();
}

function getCardRateBw()
{
	var cardRate = getCardPriceBw() /getCardQtyBw();
	
	return cardRate;
}

function getPaperCodeBw()
{
	return jQuery("#paper_code-bw").val();
}

function saveVCardBw()
{
	var multiple = 1;
	updateQtyBw();	
	if(jQuery("#v_card_names-bw").val().length > 0 )
	{
		multiple = jQuery("#v_card_names-bw").val();
	}
	
	var id = jQuery("#fancybox_id").val();
	jQuery("#category_" +id).val('Visiting Card');
	
	if(getPaperCodeBw().length > 0 )
	{
		jQuery("#details_" +id).val(getCardTypeBw());
	}
	else
	{
		jQuery("#details_" +id).val(getCardTypeBw());
	}
	
	jQuery("#qty_" +id).val(getCardQtyBw());
	jQuery("#rate_" +id).val(getCardRateBw());
	jQuery("#sub_" +id).val(getCardPriceBw());
	var cardTitle = jQuery("#original_title-bw").val();
	
	if(getCardQtyBw() >= 1000 && (cardTitle == "Premium_Texture_Card_FB" || cardTitle == "Premium_Texture_Card_SS" || cardTitle == "Economy_Texture_Card_SS" || cardTitle == "Economy_Texture_Card_FB"))
	{
		var nextId = parseInt(id ) + 1;
		
		jQuery("#details_" +nextId).val("Free Card Holder");
		jQuery("#qty_" +nextId).val(1);
		jQuery("#rate_" +nextId).val(0);
		jQuery("#sub_" +nextId).val(0);
	}
	
	jQuery.fancybox.close();
	
	return false;
}

function setPaperCodeValueBw()
{
}

function updateQtyBw()
{
	var multiple = 1;
	
	if(jQuery("#v_card_names-bw").val().length > 0 )
	{
		multiple = jQuery("#v_card_names-bw").val();
	}
	
	setFinalQtyBw(jQuery("#original_v_card_qty-bw").val() * multiple)
	setCardPriceBw(jQuery("#original_v_card_price-bw").val() * multiple);
	
	var cardTitle = jQuery("#original_title-bw").val();
	
	var paperCode = jQuery("#paper_code-bw").val();
	var printSide = getPaperPrintSideBw();
	
	if(jQuery("#paper_code-bw").val() != '' &&  jQuery("#paper_code-bw").val().length > 0) 
	{
		var finalTitle = cardTitle+"_"+paperCode+"_"+jQuery("#original_v_card_qty-bw").val()+"*"+multiple;
		
		setCardTypeBw(finalTitle);
		
		return true;
	}
	
	
	var finalTitle = cardTitle+"_"+jQuery("#original_v_card_qty-bw").val()+"*"+multiple;
	
	setCardTypeBw(finalTitle);
		
	return true;
}
